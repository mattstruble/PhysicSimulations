#include "Cube.h"
#include "Color.h"
//======================================================================
Cube::Cube(double size)
{
	m_Size = size;
}

//--------------------------------------------------------------------------------
void Cube::Draw()
{
	glColor3f(Color::AZURE.R, Color::AZURE.G, Color::AZURE.B);

	if (m_WireFrame)
	{
		glutWireCube(m_Size);
	}
	else
	{
		glutSolidCube(m_Size);
	}
}
